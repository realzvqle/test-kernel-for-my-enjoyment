VOID KiDrawCharacter(CHAR c, UINT16 x, UINT16 y, UINT16 size, UINT64 color){
    if(c > 127) return;
    INT offsety = 0;
    INT offsetx = 0;
    for(INT i = 1; i < (5 * 6) + 1; i++){
        INT truei = i - 1;
        // if this is before the drawing of the rectangle it leads to broken results
        if(truei % 5 == 0){
            offsetx = 0;
            offsety += size;
        } else offsetx += size;
        if(font[c][truei] == 0x333332){
            KiDrawRectangle(x + (offsetx), y + offsety, size, size, color);
        }
        
    }
}

VOID KiDrawString(PCHAR string, UINT16 x, UINT16 y, UINT16 size, UINT64 color){
    INT offset = 0;
    while(*string != '\0'){
        INT xmuloffset;
        // anything times 0 = 0 which leads to issues, which is why we need this
        if(offset == 0) xmuloffset = x;
        else xmuloffset = x + offset * 6;
        KiDrawCharacter(*string, xmuloffset, y, size, color);
        offset += size;
        string++;
    }
}

